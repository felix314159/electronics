uint8_t melodies_count(void)
{
    return (uint8_t)(sizeof(melody_catalog) / sizeof(melody_catalog[0]));
}

const note_t *melodies_get_notes(uint8_t index)
{
    return (const note_t *)(uintptr_t)pgm_read_word(&melody_catalog[index].notes);
}

uint8_t melodies_get_length(uint8_t index)
{
    return pgm_read_byte(&melody_catalog[index].length);
}
